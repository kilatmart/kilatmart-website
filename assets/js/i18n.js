window.KM_I18N.apply = function (lang) {
  var dict = window.KM_I18N[lang] || window.KM_I18N.en;
  var fallback = window.KM_I18N.en;

  document.documentElement.lang = lang === 'ms' ? 'ms' : 'en';

  var title = dict.meta_title || fallback.meta_title;
  document.title = title;

  var metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', dict.meta_description || fallback.meta_description);

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    var val = dict[key] !== undefined ? dict[key] : fallback[key];
    if (val !== undefined) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-html');
    var val = dict[key] !== undefined ? dict[key] : fallback[key];
    if (val !== undefined) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    var val = dict[key] !== undefined ? dict[key] : fallback[key];
    if (val !== undefined) el.setAttribute('placeholder', val);
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-aria');
    var val = dict[key] !== undefined ? dict[key] : fallback[key];
    if (val !== undefined) el.setAttribute('aria-label', val);
  });

  var subjectSelect = document.getElementById('subject');
  if (subjectSelect) {
    var opts = subjectSelect.querySelectorAll('option');
    var optKeys = ['form_opt_general', 'form_opt_support', 'form_opt_partnership', 'form_opt_rider', 'form_opt_press'];
    opts.forEach(function (opt, i) {
      if (optKeys[i]) {
        var label = dict[optKeys[i]] || fallback[optKeys[i]];
        opt.textContent = label;
        opt.value = label;
      }
    });
  }

  var ticker = document.querySelector('.ticker-track');
  if (ticker && dict.ticker_items) {
    var items = dict.ticker_items;
    var html = '';
    for (var r = 0; r < 2; r++) {
      items.forEach(function (item) {
        html += '<span>' + item + '</span><span>·</span>';
      });
    }
    ticker.innerHTML = html;
  }

  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    var btnLang = btn.getAttribute('data-lang');
    btn.classList.toggle('active', btnLang === lang);
    btn.setAttribute('aria-pressed', btnLang === lang ? 'true' : 'false');
  });

  try { localStorage.setItem('km-lang', lang); } catch (e) { /* ignore */ }
  window.KM_I18N.current = lang;
  window.dispatchEvent(new CustomEvent('km-lang-changed', { detail: { lang: lang } }));
};

window.KM_I18N.t = function (key, lang) {
  var l = lang || window.KM_I18N.current || 'en';
  var dict = window.KM_I18N[l] || window.KM_I18N.en;
  return dict[key] !== undefined ? dict[key] : window.KM_I18N.en[key];
};

window.KM_I18N.init = function () {
  var saved = 'en';
  try { saved = localStorage.getItem('km-lang') || 'en'; } catch (e) { /* ignore */ }
  if (saved !== 'en' && saved !== 'ms') saved = 'en';
  window.KM_I18N.apply(saved);

  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.addEventListener('click', function () {
      window.KM_I18N.apply(btn.getAttribute('data-lang'));
    });
  });
};
